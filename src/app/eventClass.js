class Event {
  constructor(id, fullStartDate, fullEndDate, title, description, location, color, repeat) {
    this.id = id;
    this.fullStartDate = fullStartDate;
    this.fullEndDate = fullEndDate;
    this.title = title;
    this.description = description;
    this.location = location;
    this.color = color;
    this.repeat = repeat;
  }
}